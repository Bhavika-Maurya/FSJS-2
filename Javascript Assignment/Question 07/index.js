// 07. Declare an array containing the multiple values and use lastIndexOf to determine the position of the first and last occurrence of a word "pw skills".

// Declare an array containing multiple values
const arr = ["This is a pw skills training session", "pw skills is a essential skill to have", "pw skills is in demand"];

// Use lastIndexOf() to determine the position of the last occurrence of "pw skills"
const lastIndex = arr.lastIndexOf("pw skills");
console.log("Last occurrence of 'pw skills' is at index: " + lastIndex); 

// Output: "Last occurrence of 'pw skills' is at index: 2"
